Stack <String>pila1=new Stack<String>();
Stack <String>pila2=new Stack<String>();
    
//Este metodo es para a√±adir 
public void add(){
    //Aqui se estan agregando datos a la pila, se estan jalando deltext field
    pila1.push(jTextField1.getText());
    //Aqui se muestran los datos 
    jTextField2.setText(pila1.toString());
    jTextField1.setText("");
    ultimo();
}
//aqui se esta creandoun metodo para quitar elementos usando el metodo pop
public void quitar(){
    try{
    pila1.pop();
     jTextField2.setText(pila1.toString());
     ultimo();
      }catch (Exception ex){
           JOptionPane.showMessageDialog(null, "La pila esta vacia");
       }
}
// aqui mostramos el ultimo elemento de la pila utilizando el metodo peek
public void ultimo(){
   jTextField4.setText(pila1.peek()); 
}
public void mandarpila2(){
    pila2.push(pila1.pop());
    jTextField2.setText(pila1.toString());
    jTextField3.setText(pila2.toString());
}
